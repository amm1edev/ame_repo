import asyncio

import telethon
from telethon.tl.types import Message

from .. import loader, utils


@loader.tds
class AutoForward(loader.Module):
    """–ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ –ø–µ—Ä–µ—Å—ã–ª–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∑ –∫–∞–Ω–∞–ª–æ–≤ –≤ –æ–¥–∏–Ω"""

    strings = {
        "name": "AutoForward",
        "channels_from": "–ö–∞–Ω–∞–ª—ã –∏–∑ –∫–æ—Ç–æ—Ä—ã—Ö –±—É–¥—É—Ç –ø–µ—Ä–µ—Å—Å—ã–ª–∞—Ç—å—Å—è —Å–æ–æ–±—â–µ–Ω–∏—è",
        "channel_to": "–ö–∞–Ω–∞–ª –≤ –∫–æ—Ç–æ—Ä—ã–π –±—É–¥—É—Ç –ø–µ—Ä–µ—Å—ã–ª–∞—Ç—å—Å—è —Å–æ–æ–±—â–µ–Ω–∏—è –∏–∑ –∫–∞–Ω–∞–ª–æ–≤",
    }

    async def client_ready(self, client, db):
        self._client = client
        self._db = db
        self._db.set(__name__, "status", False)

    def __init__(self):
        self.config = loader.ModuleConfig(
            loader.ConfigValue(
                "channels_from",
                [],
                lambda: self.strings("channels_from"),
                validator=loader.validators.Series(
                    validator=loader.validators.Union(
                        loader.validators.Integer(),
                    )
                ),
            ),
            loader.ConfigValue(
                "channel_to",
                None,
                lambda: self.strings("channel_to"),
                validator=loader.validators.Integer(),
            ),
        )

    @loader.command()
    async def autoforward(self, message):
        """- –≤–∫–ª/–≤—ã–∫–ª –º–æ–¥—É–ª—è"""
        status = self._db.get(__name__, "status")
        self._db.set(__name__, "status", not status)
        data = (
            "–ü–µ—Ä–µ—Å—ã–ª–∞—é" if self._db.get(__name__, "status") else "–ë–æ–ª—å—à–µ –Ω–µ –ø–µ—Ä–µ—Å—ã–ª–∞—é"
        )

        await utils.answer(
            message,
            "<emoji document_id=5416117059207572332>‚û°Ô∏è</emoji> <b>{}</b>".format(data),
        )

    @loader.watcher(only_channels=True)
    async def watcher(self, m):
        status = self._db.get(__name__, "status")
        if status:
            if m.chat_id not in self.config["channels_from"]:
                return
            await m.forward_to(self.config["channel_to"])
